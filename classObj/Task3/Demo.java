package BMI.datamember;

class BMIDataMember{
	private double weight;
	private double height;
	
	public void setWeight(double weight){
		this.weight = weight;
	}
	public void setHeight(double height){
		this.height = height;
	}
	public getWeight(){
		return this.weight;
	}
	public getHeight(){
		return this.height;
	}
}
 
